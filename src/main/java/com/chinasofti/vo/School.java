package com.chinasofti.vo;

public class School {
	private String sch_id;
	private String sch_name;
	private String sch_phone;
	private String sch_address;
	private String sch_bank;
	public String getSch_id() {
		return sch_id;
	}
	public void setSch_id(String sch_id) {
		this.sch_id = sch_id;
	}
	public String getSch_name() {
		return sch_name;
	}
	public void setSch_name(String sch_name) {
		this.sch_name = sch_name;
	}
	public String getSch_phone() {
		return sch_phone;
	}
	public void setSch_phone(String sch_phone) {
		this.sch_phone = sch_phone;
	}
	public String getSch_address() {
		return sch_address;
	}
	public void setSch_address(String sch_address) {
		this.sch_address = sch_address;
	}
	public String getSch_bank() {
		return sch_bank;
	}
	public void setSch_bank(String sch_bank) {
		this.sch_bank = sch_bank;
	}
	
	

}
