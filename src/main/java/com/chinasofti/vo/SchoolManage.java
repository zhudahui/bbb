package com.chinasofti.vo;

public class SchoolManage {
	private String sm_id;
	private String sm_pwd;
	private String sm_name;
	private String sm_school;
	private String sm_phone;
	private int flag;
	private String sm_qq;
	public String getSm_id() {
		return sm_id;
	}
	public void setSm_id(String sm_id) {
		this.sm_id = sm_id;
	}
	public String getSm_pwd() {
		return sm_pwd;
	}
	public void setSm_pwd(String sm_pwd) {
		this.sm_pwd = sm_pwd;
	}
	public String getSm_name() {
		return sm_name;
	}
	public void setSm_name(String sm_name) {
		this.sm_name = sm_name;
	}
	public String getSm_school() {
		return sm_school;
	}
	public void setSm_school(String sm_school) {
		this.sm_school = sm_school;
	}
	public String getSm_phone() {
		return sm_phone;
	}
	public void setSm_phone(String sm_phone) {
		this.sm_phone = sm_phone;
	}
	public int getFlag() {
		return flag;
	}
	public void setFlag(int flag) {
		this.flag = flag;
	}

	public String getSm_qq() {
		return sm_qq;
	}

	public void setSm_qq(String sm_qq) {
		this.sm_qq = sm_qq;
	}
}
