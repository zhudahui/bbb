package com.chinasofti.vo;

public class Course {
	private String c_id;
	private String c_name;
	private String c_school;
	private String c_teacher;
	private int c_period;
	public String getC_id() {
		return c_id;
	}
	public void setC_id(String c_id) {
		this.c_id = c_id;
	}
	public String getC_name() {
		return c_name;
	}
	public void setC_name(String c_name) {
		this.c_name = c_name;
	}
	public String getC_school() {
		return c_school;
	}
	public void setC_school(String c_school) {
		this.c_school = c_school;
	}
	public String getC_teacher() {
		return c_teacher;
	}
	public void setC_teacher(String c_teacher) {
		this.c_teacher = c_teacher;
	}
	public int getC_period() {
		return c_period;
	}
	public void setC_period(int c_period) {
		this.c_period = c_period;
	}
	
	
}
