package com.chinasofti.vo;

public class Family {
	private int f_id;
	private String s_id;
	private String f_nickname;
	private String f_name;
	private String f_company;
	private String f_phone;
	
	public int getF_id() {
		return f_id;
	}
	public void setF_id(int f_id) {
		this.f_id = f_id;
	}
	public String getS_id() {
		return s_id;
	}
	public void setS_id(String s_id) {
		this.s_id = s_id;
	}
	public String getF_nickname() {
		return f_nickname;
	}
	public void setF_nickname(String f_nickname) {
		this.f_nickname = f_nickname;
	}
	public String getF_name() {
		return f_name;
	}
	public void setF_name(String f_name) {
		this.f_name = f_name;
	}
	public String getF_company() {
		return f_company;
	}
	public void setF_company(String f_company) {
		this.f_company = f_company;
	}
	public String getF_phone() {
		return f_phone;
	}
	public void setF_phone(String f_phone) {
		this.f_phone = f_phone;
	}
	
	
}
