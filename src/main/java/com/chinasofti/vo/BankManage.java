package com.chinasofti.vo;

public class BankManage {
	private String bm_id;
	private String bm_pwd;
	private String bm_name;
	private String bm_phone;
	private String bm_bank;
	private int flag;
	public String getBm_id() {
		return bm_id;
	}
	public void setBm_id(String bm_id) {
		this.bm_id = bm_id;
	}
	public String getBm_pwd() {
		return bm_pwd;
	}
	public void setBm_pwd(String bm_pwd) {
		this.bm_pwd = bm_pwd;
	}
	public String getBm_name() {
		return bm_name;
	}
	public void setBm_name(String bm_name) {
		this.bm_name = bm_name;
	}
	public String getBm_phone() {
		return bm_phone;
	}
	public void setBm_phone(String bm_phone) {
		this.bm_phone = bm_phone;
	}
	public String getBm_bank() {
		return bm_bank;
	}
	public void setBm_bank(String bm_bank) {
		this.bm_bank = bm_bank;
	}
	public int getFlag() {
		return flag;
	}
	public void setFlag(int flag) {
		this.flag = flag;
	}
	
	

}
